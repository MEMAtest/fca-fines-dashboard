#!/usr/bin/env npx tsx
/**
 * Ingest World Bank WGI → src/data/governanceData.ts snapshot.
 *
 * WGI lives on the World Bank data360 platform. For each of the six WGI
 * dimensions we pull the percentile-rank measure (COMP_BREAKDOWN_1 = "WGI_SC",
 * 0–100, HIGHER = BETTER) for the latest year across all countries. We store
 * BOTH the per-dimension percentiles (for the Basel-style domain breakdown:
 * corruption / rule of law / political stability) AND their mean. WGI is
 * CC BY 4.0 (World Bank), attribution required.
 *
 * Run where data360api.worldbank.org is reachable:
 *   npx tsx scripts/ingest-wgi.ts            # write the snapshot
 *   npx tsx scripts/ingest-wgi.ts --dry-run  # print, don't write
 */

import { writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { COUNTRIES, getCountryByIso3 } from "../src/data/countries.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, "..", "src", "data", "governanceData.ts");
const dryRun = process.argv.includes("--dry-run");

const BASE = "https://data360api.worldbank.org/data360/data";
const INDICATORS: Array<{ code: string; key: string }> = [
  { code: "GOV_WGI_CC", key: "cc" }, // Control of Corruption
  { code: "GOV_WGI_RL", key: "rl" }, // Rule of Law
  { code: "GOV_WGI_RQ", key: "rq" }, // Regulatory Quality
  { code: "GOV_WGI_GE", key: "ge" }, // Government Effectiveness
  { code: "GOV_WGI_PV", key: "pv" }, // Political Stability & Absence of Violence
  { code: "GOV_WGI_VA", key: "va" }, // Voice & Accountability
];
const DIM_KEYS = INDICATORS.map((i) => i.key);
const YEAR = process.argv.find((a) => a.startsWith("--year="))?.split("=")[1] ?? "2023";

interface Row {
  REF_AREA: string;
  OBS_VALUE: string;
  COMP_BREAKDOWN_1: string;
}

async function fetchPercentiles(indicator: string): Promise<Map<string, number>> {
  const url =
    `${BASE}?DATABASE_ID=WB_WGI&INDICATOR=${indicator}` +
    `&COMP_BREAKDOWN_1=WGI_SC&timePeriodFrom=${YEAR}&timePeriodTo=${YEAR}&per_page=1000`;
  const map = new Map<string, number>();
  for (let attempt = 0; attempt < 4; attempt++) {
    try {
      const res = await fetch(url, { signal: AbortSignal.timeout(45_000) });
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      const json = (await res.json()) as { value?: Row[] };
      for (const r of json.value ?? []) {
        if (r.COMP_BREAKDOWN_1 !== "WGI_SC") continue; // percentile rank only
        const v = Number(r.OBS_VALUE);
        if (!Number.isNaN(v)) map.set(r.REF_AREA, v);
      }
      return map;
    } catch (err) {
      if (attempt === 3) {
        console.error(`  ${indicator} failed: ${err instanceof Error ? err.message : err}`);
        return map;
      }
      await new Promise((r) => setTimeout(r, 3000));
    }
  }
  return map;
}

type Dims = Record<string, number>;

function renderFile(dims: Record<string, Dims>, mean: Record<string, number>): string {
  const dimEntries = Object.entries(dims)
    .sort((a, b) => (mean[b[0]] ?? 0) - (mean[a[0]] ?? 0))
    .map(([iso2, d]) => {
      const parts = DIM_KEYS.filter((k) => d[k] !== undefined)
        .map((k) => `${k}: ${d[k]}`)
        .join(", ");
      return `  ${iso2}: { ${parts} },`;
    })
    .join("\n");
  const meanEntries = Object.entries(mean)
    .sort((a, b) => b[1] - a[1])
    .map(([iso2, pct]) => `  ${iso2}: ${pct},`)
    .join("\n");
  return `/**
 * World Bank Worldwide Governance Indicators (WGI) — snapshot.
 *
 * Per-dimension percentile ranks (0–100, HIGHER = BETTER governance) for the six
 * WGI dimensions, plus their mean. These drive the Basel-style scored domains of
 * the Country Risk Score (corruption, rule of law / institutions, political
 * stability). CC BY 4.0 (World Bank), attribution required.
 *
 * GENERATED by scripts/ingest-wgi.ts from the World Bank data360 API (WGI ${YEAR}
 * data). Do not edit by hand — re-run to refresh.
 */

export const GOVERNANCE_SOURCE =
  "https://www.worldbank.org/en/publication/worldwide-governance-indicators";
export const GOVERNANCE_VINTAGE = "${YEAR}";
export const GOVERNANCE_LICENCE = "CC BY 4.0 — World Bank WGI";

/** The six WGI dimensions (percentile rank 0–100, higher = better). */
export interface WgiDimensions {
  /** Control of Corruption */
  cc: number;
  /** Rule of Law */
  rl: number;
  /** Regulatory Quality */
  rq: number;
  /** Government Effectiveness */
  ge: number;
  /** Political Stability & Absence of Violence */
  pv: number;
  /** Voice & Accountability */
  va: number;
}

/** iso2 -> per-dimension WGI percentiles (dimensions missing data are omitted). */
export const GOVERNANCE_DIMENSIONS: Record<string, Partial<WgiDimensions>> = {
${dimEntries}
};

/** iso2 -> mean WGI percentile (0–100, higher = better governance). */
export const GOVERNANCE_PERCENTILE: Record<string, number> = {
${meanEntries}
};

export function getGovernancePercentile(iso2: string): number | undefined {
  return GOVERNANCE_PERCENTILE[iso2.toUpperCase()];
}

export function getGovernanceDimensions(
  iso2: string,
): Partial<WgiDimensions> | undefined {
  return GOVERNANCE_DIMENSIONS[iso2.toUpperCase()];
}

export function hasGovernanceData(iso2: string): boolean {
  return iso2.toUpperCase() in GOVERNANCE_PERCENTILE;
}
`;
}

async function main(): Promise<void> {
  console.log(`Fetching WGI ${YEAR} (6 dimensions) from data360…`);
  const perIndicator = new Map<string, Map<string, number>>();
  for (const ind of INDICATORS) {
    const m = await fetchPercentiles(ind.code);
    perIndicator.set(ind.key, m);
    console.log(`  ${ind.code} (${ind.key}): ${m.size} countries`);
  }

  // Collect every ISO3 seen, map to ISO2, store per-dimension + mean.
  const iso3s = new Set<string>();
  for (const m of perIndicator.values()) for (const k of m.keys()) iso3s.add(k);

  const dims: Record<string, Dims> = {};
  const mean: Record<string, number> = {};
  for (const iso3 of iso3s) {
    const country = getCountryByIso3(iso3);
    if (!country) continue; // skip aggregates/regions not in our reference
    const d: Dims = {};
    const vals: number[] = [];
    for (const key of DIM_KEYS) {
      const v = perIndicator.get(key)?.get(iso3);
      if (v !== undefined) {
        d[key] = Math.round(v);
        vals.push(v);
      }
    }
    if (vals.length === 0) continue;
    dims[country.iso2] = d;
    mean[country.iso2] = Math.round(vals.reduce((s, v) => s + v, 0) / vals.length);
  }

  console.log(
    `Resolved WGI for ${Object.keys(mean).length}/${COUNTRIES.length} reference countries (per-dimension + mean).`,
  );
  if (dryRun) {
    console.log(JSON.stringify({ sample: Object.entries(dims).slice(0, 3), mean: Object.entries(mean).slice(0, 3) }, null, 2));
    return;
  }
  writeFileSync(OUT, renderFile(dims, mean));
  console.log(`Wrote ${OUT}`);
}

main();
